function getDefuntsOfTheDay(date) {
  const day = String(date.getDate()).padStart(2, '0');
  const month = String(date.getMonth() + 1).padStart(2, '0');
  const dateKey = `${day}-${month}`;  // Format jj-mm pour correspondre aux données
  return defuntsData.defunts[dateKey] || [];
}

function formatStatut(statut) {
  const statuts = {
    'c': ' (coopérateur)',
    'd': ' (diacre)',
    'e': ' (évêque)',
    'f': ' (familier)',
    't': ' (tertiaire)',
    'saint': ' (par l’intercession du bx)'
  };
  return statuts[statut] || '';
}
function getDefuntsData() {
  return defuntsData;
}

function getDefuntsOfTheDay(date) {
  const month = String(date.getMonth() + 1).padStart(2, '0');
  const day = String(date.getDate()).padStart(2, '0');
  const dateKey = `${month}-${day}`;
  return defuntsData.defunts[dateKey] || [];
}

function formatStatut(statut) {
  const statuts = {
    'c': ' (coopérateur)',
    'd': ' (diacre)',
    'e': ' (évêque)',
    'f': ' (familier)',
    't': ' (tertiaire)'
  };
  return statuts[statut] || '';
}